def enrich_events_with_posts(events: list[Event], posts: list[Post]) -> None:
    posts_by_path = {post.post_path: post for post in posts}
    for event in events:
        if not event.link:
            continue
        linked_post = posts_by_path.get(event.link)
        if linked_post and linked_post.instagram_url:
            event.instagram_url = linked_post.instagram_url


