def timeline_html(posts: list[Post]) -> str:
    groups: list[str] = []
    current_month = ""
    open_group = False

    for post in posts:
        month_key = f"{post.published:%Y-%m}"
        if month_key != current_month:
            if open_group:
                groups.append("</div></section>")
            groups.append(
                f"""
                <section class="archive-group" id="archive-{month_key}" data-date-group>
                    <div class="archive-heading">
                        <span>{html.escape(format_month(post.published).title())}</span>
                    </div>
                    <div class="post-grid">
                """
            )
            current_month = month_key
            open_group = True
        groups.append(post_card_html(post))

    if open_group:
        groups.append("</div></section>")

    return "".join(groups)


