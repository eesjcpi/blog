def portal_post_title(post: Post) -> str:
    if post.generated_title and post.instagram_url:
        return f"Registro no Instagram - {post.date_label}"
    return post.title


def portal_post_excerpt(post: Post) -> str:
    generic = "Registro publicado no blog da escola."
    if post.instagram_url and post.excerpt == generic:
        return "Publicação incorporada do Instagram com registro das atividades e eventos da escola."
    return post.excerpt


