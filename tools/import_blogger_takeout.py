def institutional_post_excerpt(post: Post, limit: int = 130) -> str:
    return shorten(portal_post_excerpt(post), limit)


def find_post_by_keywords(posts: list[Post], keywords: list[str]) -> Post | None:
    normalized_keywords = [slugify(keyword) for keyword in keywords]
    for post in posts:
        haystack = slugify(" ".join([post.title, post.slug, post.excerpt, " ".join(post.labels)]))
        if any(keyword in haystack for keyword in normalized_keywords):
            return post
    return None


def select_visual_posts(posts: list[Post], *, limit: int = 6, exclude_slugs: set[str] | None = None) -> list[Post]:
    exclude_slugs = exclude_slugs or set()
    selected: list[Post] = []
    for post in posts:
        if post.slug in exclude_slugs or not post.cover:
            continue
        selected.append(post)
        if len(selected) == limit:
            break
    return selected


def institutional_hero_post(posts: list[Post]) -> Post:
    visual_posts = [post for post in posts if post.cover]
    return (
        find_post_by_keywords(visual_posts, ["arraia", "mostra cultural", "projeto leitura"])
        or next((post for post in visual_posts if not post.generated_title), posts[0])
    )


