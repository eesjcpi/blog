def slugify(value: str, fallback: str = "item") -> str:
    value = unicodedata.normalize("NFKD", value)
    value = value.encode("ascii", "ignore").decode("ascii")
    value = value.lower()
    value = re.sub(r"[^a-z0-9]+", "-", value).strip("-")
    return value or fallback


def text_from_html(value: str) -> str:
    value = re.sub(r"<script\b[^>]*>.*?</script>", " ", value, flags=re.I | re.S)
    value = re.sub(r"<style\b[^>]*>.*?</style>", " ", value, flags=re.I | re.S)
    value = re.sub(r"<[^>]+>", " ", value)
    value = html.unescape(value).replace("\xa0", " ")
    return re.sub(r"\s+", " ", value).strip()


def shorten(value: str, limit: int = 180) -> str:
    value = re.sub(r"\s+", " ", value).strip()
    if len(value) <= limit:
        return value
    clipped = value[:limit].rsplit(" ", 1)[0].rstrip(",.;:-")
    return f"{clipped}..."


def parse_date(value: str | None) -> datetime:
    if not value:
        return datetime(1970, 1, 1)
    value = value.replace("Z", "+00:00")
    return datetime.fromisoformat(value)


def format_date(value: datetime) -> str:
    return value.strftime("%d/%m/%Y")


def format_month(value: datetime) -> str:
    return f"{MONTHS[value.month - 1]} {value.year}"


def zip_basename(name: str) -> str:
    return posixpath.basename(name.rstrip("/"))


def image_key(value: str) -> str:
    return unquote(html.unescape(value)).replace("\\", "/").split("/")[-1].lower()


def image_name_from_url(value: str) -> str | None:
    parsed = urlparse(html.unescape(value))
    name = posixpath.basename(parsed.path)
    if not name:
        return None
    name = unquote(name)
    ext = os.path.splitext(name)[1].lower()
    if ext not in IMAGE_EXTENSIONS:
        return None
    return name


def extract_instagram_url(content: str) -> str | None:
    match = re.search(r"https://(?:www\.)?instagram\.com/(?:p|reel|tv)/[^\"'<\s]+", content, flags=re.I)
    if not match:
        return None
    return html.unescape(match.group(0)).rstrip("/")


def clean_content(content: str) -> str:
    content = re.sub(r"<script\b[^>]*>.*?</script>", "", content, flags=re.I | re.S)
    content = re.sub(r"<style\b[^>]*>.*?</style>", "", content, flags=re.I | re.S)
    content = re.sub(r"<!--\s*more\s*-->", "", content, flags=re.I)
    content = re.sub(r'href="([^"]+)"_blank"', r'href="\1" target="_blank"', content)
    content = re.sub(r"<br\s*/?>\s*(<br\s*/?>\s*)+", "<br>", content, flags=re.I)
    return content.strip()


def add_lazy_loading(content: str) -> str:
    def replace(match: re.Match[str]) -> str:
        tag = match.group(0)
        if re.search(r"\sloading=", tag, flags=re.I) is None:
            tag = re.sub(r"\s*/?>$", ' loading="lazy">', tag)
        if re.search(r"\salt=", tag, flags=re.I) is None:
            tag = re.sub(r"\s*/?>$", ' alt="">', tag)
        return tag

    return re.sub(r"<img\b[^>]*>", replace, content, flags=re.I)


def rewrite_image_urls(content: str, image_map: dict[str, str], prefix: str) -> tuple[str, str | None, int]:
    first_image: str | None = None
    missing = 0

    def replace(match: re.Match[str]) -> str:
        nonlocal first_image, missing
        attr = match.group(1)
        quote = match.group(2)
        value = match.group(3)
        image_name = image_name_from_url(value)
        if not image_name:
            return match.group(0)
        local = image_map.get(image_key(image_name))
        if local:
            if first_image is None:
                first_image = local
            return f'{attr}={quote}{prefix}{local}{quote}'
        missing += 1
        return match.group(0)

    rewritten = re.sub(r"\b(src|href)=(['\"])([^'\"]+)\2", replace, content, flags=re.I)
    return rewritten, first_image, missing


def find_feed(zf: zipfile.ZipFile) -> str:
    feeds = [info.filename for info in zf.infolist() if info.filename.endswith("/feed.atom")]
    if not feeds:
        raise RuntimeError("Nenhum feed.atom foi encontrado no backup.")
    return feeds[0]


def extract_images(zf: zipfile.ZipFile, output_dir: Path) -> dict[str, str]:
    output_dir.mkdir(parents=True, exist_ok=True)
    image_map: dict[str, str] = {}
    used_names: set[str] = set()

    for info in zf.infolist():
        name = zip_basename(info.filename)
        ext = os.path.splitext(name)[1].lower()
        if ext not in IMAGE_EXTENSIONS:
            continue

        stem = os.path.splitext(name)[0]
        safe_stem = slugify(stem, "imagem")
        safe_name = f"{safe_stem}{ext}"
        counter = 2
        while safe_name in used_names:
            safe_name = f"{safe_stem}-{counter}{ext}"
            counter += 1

        used_names.add(safe_name)
        target = output_dir / safe_name
        target.write_bytes(zf.read(info.filename))
        image_map.setdefault(image_key(name), f"assets/img/{safe_name}")

    return image_map


def build_posts(feed_xml: str, image_map: dict[str, str]) -> tuple[str, list[Post], int]:
    root = ET.fromstring(feed_xml)
    blog_title = (root.findtext(f"{{{ATOM_NS}}}title") or "Blog da Escola").strip()
    posts: list[Post] = []
    slugs: set[str] = set()
    missing_images = 0

    for entry in root.findall(f"{{{ATOM_NS}}}entry"):
        entry_type = entry.findtext(f"{{{BLOGGER_NS}}}type")
        status = entry.findtext(f"{{{BLOGGER_NS}}}status")
        if entry_type != "POST" or status != "LIVE":
            continue

        published = parse_date(entry.findtext(f"{{{ATOM_NS}}}published"))
        content = entry.findtext(f"{{{ATOM_NS}}}content") or ""
        content = clean_content(content)
        rewritten, cover, missing = rewrite_image_urls(content, image_map, "")
        missing_images += missing
        rewritten = add_lazy_loading(rewritten)
        instagram_url = extract_instagram_url(rewritten)

        excerpt_source = text_from_html(rewritten)
        excerpt = shorten(excerpt_source) or "Registro publicado no blog da escola."
        raw_title = (entry.findtext(f"{{{ATOM_NS}}}title") or "").strip()
        generated_title = not bool(raw_title)
        title = raw_title or shorten(excerpt_source, 72) or f"Publicação de {format_date(published)}"

        labels: list[str] = []
        for category in entry.findall(f"{{{ATOM_NS}}}category"):
            term = (category.attrib.get("term") or "").strip()
            if term and "blogger.com" not in term and term not in labels:
                labels.append(term)

        author = entry.findtext(f"{{{ATOM_NS}}}author/{{{ATOM_NS}}}name") or "EE São José"
        base_slug = f"{published:%Y-%m-%d}-{slugify(title, 'publicacao')}"
        slug = base_slug
        counter = 2
        while slug in slugs:
            slug = f"{base_slug}-{counter}"
            counter += 1
        slugs.add(slug)

        posts.append(
            Post(
                title=title,
                slug=slug,
                published=published,
                date_label=format_date(published),
                author=author.strip(),
                excerpt=excerpt,
                content=rewritten,
                cover=cover,
                labels=labels,
                post_path=f"posts/{slug}.html",
                generated_title=generated_title,
                instagram_url=instagram_url,
            )
        )

    posts.sort(key=lambda post: post.published, reverse=True)
    return blog_title, posts, missing_images


def prefixed_content(content: str, prefix: str) -> str:
    if not prefix:
        return content
    return re.sub(r'\b(src|href)=(["\'])assets/', rf'\1=\2{prefix}assets/', content)


def archive_items(posts: list[Post]) -> list[tuple[str, str, int]]:
    counts: dict[str, int] = {}
    labels: dict[str, str] = {}
    for post in posts:
        key = f"{post.published:%Y-%m}"
        counts[key] = counts.get(key, 0) + 1
        labels[key] = format_month(post.published)
    return [(key, labels[key], counts[key]) for key in sorted(counts.keys(), reverse=True)]


def parse_event_date(value: str) -> tuple[datetime | None, str]:
    if not value:
        return None, "Data a definir"
    try:
        parsed = datetime.fromisoformat(value)
        return parsed, format_date(parsed)
    except ValueError:
        return None, value


def load_events(project_dir: Path) -> list[Event]:
    events_path = project_dir / "data" / "eventos.json"
    if not events_path.exists():
        return []

    raw_data = json.loads(events_path.read_text(encoding="utf-8"))
    raw_events = raw_data.get("eventos", raw_data) if isinstance(raw_data, dict) else raw_data
    events: list[Event] = []

    for raw_event in raw_events:
        if not isinstance(raw_event, dict):
            continue
        title = str(raw_event.get("titulo") or raw_event.get("title") or "").strip()
        if not title:
            continue
        date_value = str(raw_event.get("data") or raw_event.get("date") or "").strip()
        parsed_date, date_label = parse_event_date(date_value)
        events.append(
            Event(
                title=title,
                date=date_value,
                date_label=date_label,
                category=str(raw_event.get("categoria") or raw_event.get("category") or "Evento").strip(),
                status=str(raw_event.get("status") or "Realizado").strip(),
                summary=str(raw_event.get("resumo") or raw_event.get("summary") or "").strip(),
                location=str(raw_event.get("local") or raw_event.get("location") or "").strip(),
                image=str(raw_event.get("imagem") or raw_event.get("image") or "").strip() or None,
                link=str(raw_event.get("link") or "").strip() or None,
                instagram_url=None,
            )
        )

    events.sort(key=lambda event: parse_event_date(event.date)[0] or datetime.min, reverse=True)
    return events


