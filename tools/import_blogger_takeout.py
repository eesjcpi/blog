def write_portal_post_pages(project_dir: Path, blog_title: str, posts: list[Post]) -> None:
    posts_dir = project_dir / "posts"
    posts_dir.mkdir(exist_ok=True)

    for index, post in enumerate(posts):
        previous_post = posts[index + 1] if index + 1 < len(posts) else None
        next_post = posts[index - 1] if index > 0 else None
        more_posts = [item for item in posts[:6] if item.slug != post.slug][:3]
        nav_items = []
        if previous_post:
            nav_items.append(f'<a href="{html.escape(previous_post.slug)}.html">Publicação anterior</a>')
        nav_items.append('<a href="/#arquivo">Voltar ao acervo</a>')
        if next_post:
            nav_items.append(f'<a href="{html.escape(next_post.slug)}.html">Próxima publicação</a>')

        content = prefixed_content(post.content, "../")
        title = portal_post_title(post)
        excerpt = portal_post_excerpt(post)
        body = f"""
        <main class="portal-main">
            <div class="post-page-grid">
                <article class="post-article" data-post data-search-text="{html.escape((title + ' ' + excerpt).lower())}">
                    <header class="post-hero">
                        <a class="back-link" href="/#arquivo">Acervo</a>
                        <span>{html.escape(post.date_label)}</span>
                        <h1>{html.escape(title)}</h1>
                        <p>{html.escape(excerpt)}</p>
                        <div class="pill-row">{portal_tags_html(post.labels)}</div>
                    </header>
                    <div class="post-content">
                        {content}
                    </div>
                    <nav class="post-navigation" aria-label="Navegação entre publicações">
                        {''.join(nav_items)}
                    </nav>
                </article>
                <aside class="related-panel" aria-label="Publicações recentes">
                    <div class="panel-title">
                        <span>Continue lendo</span>
                        <h2>Recentes</h2>
                    </div>
                    <div class="related-list">
                        {''.join(portal_headline_item_html(item, "../") for item in more_posts)}
                    </div>
                </aside>
            </div>
        </main>
        """
        document = portal_shell(
            blog_title=blog_title,
            title=f"{title} | {blog_title}",
            css_href="/assets/css/style-20260619.css",
            script_href="/assets/js/script-20260619.js",
            body_html=body,
            prefix="../",
            active="posts",
            page_path=f"/posts/{post.slug}.html",
            description=excerpt or SITE_DESCRIPTION,
            og_type="article",
        )
        (posts_dir / f"{post.slug}.html").write_text(document, encoding="utf-8")


